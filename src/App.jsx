function App() {
  return (
    <div className={styles.container}>
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return <h1>Fast React Pizza Co.</h1>;
}

function Menu() {
  const pizzas = pizzaData;
  const pizzaNum = pizzas.length;
  return (
    <main className={styles.menu}>
      <h2>Our Menu</h2>
      {pizzaNum > 0 ? (
        <>
          <p>
            Authentic Italian cuisine. 6 creative dishes to choose from. All
            from our stone oven, all organic, all delicious.
          </p>
          <ul className={styles.pizzas}>
            {pizzas.map((pizza) => (
              <Pizza pizzaObj={pizza} key={pizza.name} />
            ))}
          </ul>
        </>
      ) : (
        <p>We are still working on mneu. Please come back later</p>
      )}
    </main>
  );
}

function Pizza({ pizzaObj }) {
  return (
    <li
      className={`${styles.pizza} ${pizzaObj.soldOut ? styles.soldout : ""} `}
    >
      <img src={pizzaObj.photoName} alt={pizzaObj.name} />
      <div>
        <h3>{pizzaObj.name}</h3>
        <p>{pizzaObj.ingredients}</p>
        <span>{pizzaObj.soldOut ? "SOLD OUT" : pizzaObj.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 12;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;

  // if (hour >= openHour && hour <= closedHour) alert("we are currently Open");
  // else alert("We are closed");

  return (
    <footer className={styles.footer}>
      {isOpen ? (
        <div className={styles.order}>
          <p>
            We are open until {closeHour}:00. Come visit us or order online.
          </p>
          <button className={styles.btn}>Order</button>
        </div>
      ) : (
        <p>
          We are happy to welcome you between {closeHour}:00 and {openHour}:00{" "}
        </p>
      )}
    </footer>
  );
}

export default App;
